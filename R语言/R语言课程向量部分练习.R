#基本运算
X<-c(-1,0,2);
Y<-c(3,8,2);
X*Y        #对应坐标相乘(返回向量)
X%*%Y      #向量的点积(返回值)
exp(X)     #指数运算
sqrt(Y)    #开平方根

#向量相关的函数

#最值
X<-c(10,2,6,8,3,4,-2,1,0,5);
min(X)  #求最大值
max(X)  #求最小值

#求和，求积函数
X<-c(10,2,6,8,3,4,-2,1,0,5);
sum(X)      #求和函数
cumsum(X)   #返回累计和
prod(X)     #求积函数
cumprod(X)  #返回累计积

#向量个数与逆序
X<-c(10,2,6,8,3,4,-2,1,0,5);
length(X)   #求向量的长度
rev(X)      #求向量的逆序

#向量的中位数，均值，方差，标准差和次序统计量
X<-c(10,2,6,8,3,4,-2,1,0,5);
median(X)   #求向量的中位数
mean(X)     #求向量的均值
var(X)      #求向量的方差
sd(X)       #求向量的标准差
sort(X,decreasing = TRUE)     #对向量排序,decreasing = TRUE表示倒序，去掉表示升序
order(X,decreasing = TRUE)    #返回向量排序后的下标,decreasing = TRUE表示倒序，去掉表示升序

#产生有规律的序列
X<-1:30        #a:b等差为1，逐项加1，只对a，无论b是整数还是实数，运算优于四则
seq(-5,5,by=0.2)              #产生更一般的数列
seq(length=5,from=-1,by=2)    #生成长度为5，从1开始，公差为2的等差数列
rep(X,3)       #对向量重复3次
rep(X,each=3)  #对向量中的每个元素重复3次

#逻辑向量
z<-c(FALSE,TRUE,F,T)       #FALSE与TRUE为逻辑向量
all(c(1,2,3,4,5,6,7)>5)    #判断向量中所有的元素是否都大于5
any(c(1,2,3,4,5,6,7)>5)    #判断向量中是否有大于5的元素

#缺失数据
X<-c(1,2,NA,NaN,Inf);
is.na(X)                   #用于检验数据中是否有缺失数据NA
is.nan(X)                  #用于检验数据中是否有不确定数据NAN
is.finite(X)               #用于检验数据中是否有限无限为Inf

#字符串向量
