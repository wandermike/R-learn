#复数向量
x<-seq(-pi,pi,by=pi/10);
y<-sin(x);
z<-complex(re=x,im=y)    #生成复数向量re为实部，im为虚部
plot(z)                  #在图中画出向量中的点
lines(z)                 #在图中将向量中的点连成线

#向量的下标运算
X<-c(6,3,2,4);
X[2]                #直接输入下标
(c(6,3,2)+2)[3]     #对整体作用
X[2]<-23            #改变特定的位置的值
X[c(1,3)]<-c(12,45)
X[X>3]              #返回那个符合条件的向量中的元素下标

